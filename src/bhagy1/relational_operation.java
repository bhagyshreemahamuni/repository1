package bhagy1;

public class relational_operation
{
	public static void main(String args[])
	{
		int a=7, b=11;
		System.out.println("a="+a+" and value of b="+b);

		System.out.println(a==b);
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a!=b);
		
	}

}
