package loopingstatement;

public class forloopexample5 {
	//program to print number from 20 to 1
	//starting point 20
	//end point 1
	//decrement operator
	public static void main(String args[])
{
	for(int a=20;a>=1;a--)
	{
		System.out.println(a);
		
	}
	
	//program for print in format 5 10 15 20 .....40
	for(int i=5;i<=40;i=i+5)
	{
		System.out.println(i);
	}
	}
}
