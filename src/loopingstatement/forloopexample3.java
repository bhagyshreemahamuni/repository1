package loopingstatement;

public class forloopexample3 {
	//program for print even number 1 to 200
	//starting point 1
	//end point 200
	//increment operator

	public static void main(String[] args) 
	{
		
		for(int i=1;i<=200;i++)
		{
		
			if(i%2==0)
			{
				System.out.println(i+" is even number");
			}
		}
	}
}